<h2>
    <span>What now?</span>
</h2>
<p>
    <span>What happened to the Gentiles when they heard the message that Peter preached? They believed. When they believed, they received the Holy Spirit (Acts 10:44-45). When a person repents and believes in Jesus, the Holy Spirit of God makes his home in that believer and dwells in him forever.&nbsp;</span>
</p>
<p>
    <span>These Gentiles were also baptized (Acts 10:47-48). This baptism signified their new union with Jesus, their death to their old life, and the beginning of their new life (Romans 6:3-4). Being baptized, they joined the church and began their new Christian life.</span>
