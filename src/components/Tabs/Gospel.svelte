<p>
    <span>The Gospel is the good news of Jesus Christ. The message of the Gospel is presented in many places in the Bible. We will look at the message as told by the apostle Peter in Acts 10:34-43.&nbsp;</span>
</p>
<blockquote>
    <p>
        <span><i>So Peter opened his mouth and said: “Truly I understand that God shows no partiality, but in every nation anyone who fears him and does what is right is acceptable to him. As for the word that he sent to Israel, preaching good news of peace through Jesus Christ (he is Lord of all), you yourselves know what happened throughout all Judea, beginning from Galilee after the baptism that John proclaimed: how God anointed Jesus of Nazareth with the Holy Spirit and with power. He went about doing good and healing all who were oppressed by the devil, for God was with him. And we are witnesses of all that he did both in the country of the Jews and Jerusalem. They put him to death by hanging him on a tree, but God raised him on the third day and made him to appear, not to all the people but to us who had been chosen by God as witnesses, who ate and drank with him after he rose from the dead. And he commanded us to preach to the people and to testify that he is the one appointed by God to be judge of the living and the dead. To him all the prophets bear witness that everyone who believes in him receives forgiveness of sins through his name.</i></span>
    </p>
</blockquote>
<p>
    <span>Peter delivered this message to a group of Gentiles (non-Jews) a few years after the death and resurrection of Jesus. This is the message that these Gentiles needed to believe in order to be saved by God and brought into God's family. This is the message that you must believe in order to be saved from the penalty of sin.</span>
</p>
<p>
    <span>Let’s go through the parts of this Gospel message and give a brief explanation for each.</span>
</p>
<blockquote>
    <p>
        <span><i>Truly I understand that God shows no partiality, but in every nation anyone who fears him and does what is right is acceptable to him.</i></span>
    </p>
</blockquote>
<p>
    <span>This message is for everyone who will hear: Jew, Gentile, man, woman, child, dark-skinned, light-skinned, prisoner, free, rich, poor, able-bodied, and disabled. God is the Creator of every person, and he extends the offer of salvation to all people without distinction.</span>
</p>
<blockquote>
    <p>
        <span><i>As for the word that he sent to Israel, preaching good news of peace through Jesus Christ (he is Lord of all),</i></span>
    </p>
</blockquote>
<p>
    <span>Two thousand years ago, God sent his good news of peace to the people of Israel. He did this by sending his Son Jesus to the earth to preach the message of peace to the people. The people needed this message because they did not have peace with God. Their sins against God had caused them to be at enmity with God. Listen to what the prophet Isaiah said about the people of Israel: “your iniquities have made a separation between you and your God, and your sins have hidden his face from you so that he does not hear” (Isaiah 59:2). Israel’s sins had caused a separation between them and God. Our sins cause the same problem: “For all have sinned and fall short of the glory of God” (Romans 3:23).</span>
</p>
<p>
    <span>The good news is a message of restoration to God and peace with him. As Peter states, this can only happen through Jesus Christ.&nbsp;</span>
</p>
<p>
    <span>Peter also states, in parenthesis, that Jesus is Lord of all. Jesus is the master and owner of everything in the universe, including your body and soul.&nbsp;</span>
</p>
<blockquote>
    <p>
        <span><i>you yourselves know what happened throughout all Judea, beginning from Galilee after the baptism that John proclaimed:&nbsp;</i></span>
    </p>
</blockquote>
<p>
    <span>Jesus was famous in Israel. Because Peter was speaking only a few years after Jesus’ resurrection and ascension to heaven, these Gentiles listening to Peter’s message were already familiar with the life of Jesus. But you might not be! The history of Jesus’ life has been recorded in Scripture, in the books of Matthew, Mark, Luke, and John. You will be greatly blessed to read one or all of these books in a literal Bible translation (such as the ESV, NASB, LSB, CSB, BSB with links). I recommend starting with Mark or John!</span>
</p>
<blockquote>
    <p>
        <span><i>how God anointed Jesus of Nazareth with the Holy Spirit and with power.&nbsp;</i></span>
    </p>
</blockquote>
<p>
    <span>This clause is one of many passages in Scripture alluding to the triunity of God. God the Father anointed Jesus the Son with the Holy Spirit. God is one in essence and three in persons.&nbsp;</span>
</p>
<p>
    <span>This also shows that Jesus was not a rogue man doing what he pleased. He was anointed by God (Matthew 3:16-17).&nbsp;</span>
</p>
<blockquote>
    <p>
        <span><i>He went about doing good and healing all who were oppressed by the devil, for God was with him.</i></span>
    </p>
</blockquote>
<p>
    <span>In his earthly ministry, Jesus showed both the compassion and the power of God by doing good for others through miracles.</span>
</p>
<blockquote>
    <p>
        <span><i>And we are witnesses of all that he did both in the country of the Jews and in Jerusalem.</i></span>
    </p>
</blockquote>
<p>
    <span>Peter was one of Jesus’ disciples who walked with him, lived with him, and learned from him during Jesus’ earthly ministry. Peter was one of twelve “apostles” who walked with Jesus and were sent out by Jesus to be leaders in the church and to proclaim the truth.</span>
</p>
<blockquote>
    <p>
        <span><i>They put him to death by hanging him on a tree, but God raised him on the third day</i></span>
    </p>
</blockquote>
<p>
    <span>Both Jewish and Roman leaders conspired against Jesus to execute him on false charges (Luke 22:47-23:25). Jesus was killed and buried, but God raised him on the third day. Jesus rose from the dead and is still alive today.</span>
</p>
<blockquote>
    <p>
        <span><i>and made him to appear, not to all the people but to us who had been chosen by God as witnesses, who ate and drank with him after he rose from the dead.</i></span>
    </p>
</blockquote>
<p>
    <span>Jesus appeared alive for 40 days after his resurrection (Acts 1:1-3; 1 Corinthians 15:3-8). During this time, he commissioned men to preach the Gospel to the world (Acts 1:8).</span>
</p>
<blockquote>
    <p>
        <span><i>And he commanded us to preach to the people and to testify that he is the one appointed by God to judge the living and the dead.</i></span>
    </p>
</blockquote>
<p>
    <span>Jesus is the one appointed by God to judge the living and the dead. What does this mean? Man has sinned against God. God is perfectly just, so he must punish people for that sin. God will punish those who are living now, and God will punish those who have already died years ago. Jesus, the Son of God, is the one who will execute these judgments. This is the “bad news” of the Gospel; it is the reason that people need Jesus to be their Savior. Everyone in the world is indebted to God as a sinner and deserves to face his judgment.</span>
</p>
<blockquote>
    <p>
        <span><i>To him all the prophets bear witness that everyone who believes in him receives forgiveness of sins through his name.</i></span>
    </p>
</blockquote>
<p>
    <span>How can one avoid judgment, be reconciled to God, and be at peace with him? Believe in Jesus. “Everyone who believes in him receives forgiveness of sins through his name.” If you believe in Jesus, you will be forgiven by God and will not be held guilty for your sins against him.&nbsp;</span>
</p>
<p>
    <span>This is not a truth that Peter invented. This is what Jesus himself taught (John 3:16). This was also the message of the prophets of God who came before Jesus. They bore witness to the truth that God would send a Messiah into the world to save sinful humanity and restore believers back to God.&nbsp;</span>
</p>
<p>
    <span>Allow me to summarize the passage in my own words. God is the Creator of the heavens and earth. He created human beings, and he shows no partiality between them based on ethnicity, gender, age, or ability. He wants the message of salvation to be proclaimed to all people without distinction. Jesus is the Son of God and the Lord of all. He came to the earth, was anointed by the Holy Spirit, and ministered in the power of God by doing miracles for the good of the people. Though Jesus was without sin, he was put to death by the people. He was buried and was raised by God on the third day. He appeared to many people and commissioned them to be his witnesses and share the good news. He ascended to heaven and will one day return to judge all people. The Scriptures bear witness to the beautiful truth that everyone who believes in Jesus will receive forgiveness of sins. The only way to be at peace with God is to repent and believe in Jesus. Whoever does not believe in Jesus is not accepted by God and will receive God’s justice on the day that Jesus returns as the Judge.</span>
</p>
<p>
    <span>This is the Gospel of Jesus Christ. Jesus commissioned his church to send this message out into the world. This message has now made its way to you. Will you believe in him for the forgiveness of your sins?</span>
