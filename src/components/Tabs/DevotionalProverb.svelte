        The body of the book of Proverbs begins in chapter 10 verse 1. The previous nine chapters are considered the book’s introduction. I will not be writing a commentary on chapters 1 through 9, so I encourage anyone who uses the website to read and reread Proverbs 1-9 on your own! I reference these chapters 
    </p>
</div>
<div>
    <h3>
        Reading in Order
    </h3>
</div>
<div>
    <p>
        I try to write each devotional as an independent unit that doesn’t require any previous knowledge. Thus, it is possible to read through these devotionals in a random order. There is nothing wrong with that! However, I recommend beginning with Proverbs 10:1 and reading through the devotionals in order. I recommend this because I often give much more explanation for a topic the first time I write about it. I also recommend this because of the fact that Solomon was intentional in how he arranged the individual proverbs (though we don’t always know the reason for his arrangement!).
    </p>
</div>
<div>
    <h3>Consider</h3>
</div>
<div>
    <p>
        My commentary on each proverb is <u>not definitive</u>. It is based on my study of the proverb as well as my limited experience. I was selective in what I wrote and chose not to attempt an exhaustive explanation of each subject. With each proverb, there is certainly more you can consider beyond what I wrote. There are certain implications and applications that I missed or omitted. Therefore, I encourage you to consider each proverb beyond my limited explanation. If it is more profitable for you to think through the proverbs without reading this devotional, I encourage you to do so! I certainly got much joy and benefit from doing that.
    </p>
</div>
<div>
    <h3>
        Teaching Children
    </h3>
</div>
<div>
    <p>
        The proverbs are useful to any person at any age, but they are <i>especially</i> helpful for a child in his preteen and teen years. Therefore, I recommend using these devotionals as a tool for walking through the proverbs with your preteen or teen.
    </p>
</div>
