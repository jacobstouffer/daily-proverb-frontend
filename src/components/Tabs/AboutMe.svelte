    <h3>
        The author
    </h3>
</div>
<div>
    <p>
        My name is Jacob Stouffer. I live in California with my wonderful wife, Mariana. We are members of Grace Community Church. I became a Christian at age 30. Before then, I was what Solomon would call a fool. This means that I had 30 years of experience with foolish thinking, speaking, looking, and walking. I was saturated in habits of foolishness. But God, in his kindness, gave me a new life. The book of Proverbs has been instrumental in bringing me out of foolish and wicked habits and helping me walk according to God’s right path.
    </p>
</div>
<div>
    <p>
        I have been and continue to be instructed, encouraged, and challenged by the Proverbs. And I love using the Proverbs to instruct, encourage, and challenge others. To that end, I have set out to write a devotional for each of the proverbs from chapters 10 through 29. In writing these devotionals, I endeavor to present the truth of God’s Word with accuracy, clarity, and conviction. I endeavor to teach the truth of God’s Word only after it has challenged and impacted my own heart (Colossians 3:16).
    </p>
</div>
